def strip_html_keep_breaks(s):
    """Strip HTML tags but preserve the structure for matching."""
    s = re.sub(r'<[^>]+>', '', s)
    s = s.replace('&lt;', '<').replace('&gt;', '>').replace('&amp;', '&').replace('&quot;', '"').replace('&#39;', "'")
    s = re.sub(r'\s+', ' ', s).strip()
    return s


P_RE = re.compile(r'<p(?:>|\s[^>]*>)(.*?)</p>', re.DOTALL)


def find_docblocks(text):
    blocks = []
    pos = 0
    while True:
        m = re.search(r"<div class=['\"]docblock['\"]>", text[pos:])
        if not m:
            break
        start = pos + m.start()
        depth = 1
        i = pos + m.end()
        while i < len(text) and depth > 0:
            next_open = text.find("<div", i)
            next_close = text.find("</div>", i)
            if next_close == -1:
                return blocks
            if next_open != -1 and next_open < next_close:
                depth += 1
                i = next_open + 4
            else:
                depth -= 1
                i = next_close + 6
        end = i - 6
        blocks.append((start, end, text[start:end]))
        pos = end
    return blocks


def main():
    """Apply all PAIRS across all sync/* files using text-based matching."""
    total_replaced = 0
    total_files = 0
    for root, dirs, files in os.walk(SYNC_DIR):
        for f in sorted(files):
            if not f.endswith('.html'):
                continue
            full = os.path.join(root, f)
            rel = os.path.relpath(full, TOKIO_DIR)
            with open(full, 'rb') as fp:
                raw_bytes = fp.read()
            raw_orig = raw_bytes
            raw = raw_bytes.decode('utf-8')
            file_modified = False

            for file_filter, en_text, zh_text in PAIRS:
                if file_filter not in rel:
                    continue
                # Find all <p> that contain en_text
                # Use a fresh regex finditer each time
                for p_match in list(P_RE.finditer(raw)):
                    p_inner = p_match.group(1)
                    stripped = strip_html_keep_breaks(p_inner)
                    if stripped == en_text or stripped.startswith(en_text):
                        old_p = p_match.group(0)
                        # Build new <p>: preserve the <p> tag itself
                        p_open_m = re.match(r'<p(?:>|\s[^>]*>)', old_p)
                        p_open = p_open_m.group(0)
                        new_p = p_open + zh_text + '</p>'
                        raw = raw[:p_match.start()] + new_p + raw[p_match.end():]
                        total_replaced += 1
                        file_modified = True
                        break  # one match per pair

            if file_modified:
                # preserve original line endings
                if b'\r\n' in raw_orig:
                    new_bytes = raw.replace('\n', '\r\n').encode('utf-8')
                else:
                    new_bytes = raw.encode('utf-8')
                with open(full, 'wb') as fp:
                    fp.write(new_bytes)
                total_files += 1
                print(f"  Updated: {rel}")
    print(f"\nTotal replacements: {total_replaced} in {total_files} files")


if __name__ == '__main__':
    main()
